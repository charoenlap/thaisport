<p>GO Sport อาจเปลี่ยนแปลงเนื้อหาในเว็บไซต์ได้ทุกเวลาโดยไม่แจ้งให้ทราบล่วงหน้า</p>



<p>ไม่มีการรับประกันสำหรับเว็บไซต์และ/หรือเนื้อหา</p>

<p>เว็บไซต์นี้และเนื้อหาต่าง ๆ ถูกนำเสนอ “ตามสภาพ” และ “ตามที่มี” GO Sport ไม่รับประกันความเที่ยงตรง, พอเพียงหรือครบถ้วนของเว็บไซต์นี้ และ/หรือเนื้อหาต่าง ๆ และปฎิเสธความรับผิดชอบต่อความผิดพลาดหรือการละเลยในเว็บไซต์นี้ และ/หรือเนื้อหา ไม่รับรองทุกกรณีทั้งการเอ่ยถึง, แสดงออก หรืออ้างความชอบด้วยกฎหมาย รวมถึงแต่ไม่จำกัดเพียงการรับประกันการละเมิดสิทธิ์บุคคลที่สาม, ชื่อ, ความสามารถในการซื้อขาย, ความพอใจในคุณภาพ และ/หรือความเหมาะสมต่อจุดประสงค์จำเพาะใดๆ ในการอ้างถึงเว็บไซต์นี้หรือเนื้อหาต่างๆ
GO Sport ไม่รับประกันว่าเว็บไซต์นี้ และ/หรือเนื้อหา จะมีให้โดยไม่ถูกขัดขวาง หรือปราศจากข้อผิดพลาด หรือจะมีการแก้ไขข้อผิดพลาดใดๆ นอกจากนี้ ยังไม่รับประกันว่าเว็บไซต์นี้และเนื้อหาจะปราศจากไวรัสหรืออันตรายอื่นๆ, โค้ด, โปรแกรม หรือมาโครที่ผิดพลาดหรือมุ่งร้าย 
 

<p>การเชื่อมโยงสู่เว็บไซต์ภายนอก</p>

<p>GO Sport ไม่รับผิดชอบต่อความปลอดภัย, คุณภาพ หรือความถูกต้องของเว็บไซต์ใด ๆ ที่อาจเข้าถึงได้ผ่านเว็บไซต์นี้ เว็บไซต์ที่เชื่อมต่อและเข้าถึงได้ผ่านเว็บไซต์นี้ เป็นเว็บไซต์อิสระ ที่ GO </p>
<p>Sport ไม่มีอำนาจควบคุมทั้งทางการเงิน, การบรรณาธิการ หรือทางอื่นทางใด และไม่ได้มีการรับรองโดย GO Sport</p>



<p>ความปลอดภัยและการป้องกันไวรัส</p>

<p>GO Sport ไม่รับผิดชอบต่อความเสียหายใดๆ ทั้งทางตรง, ทางอ้อม, เป็นผลต่อเนื่อง, ทางพิเศษอื่นๆ หรือทางใดๆ อันเป็นผลจากการได้รับการส่งไวรัส, เวิร์ม, โทรจัน หรือเครื่องมือสร้างความเสียหายใดๆ ผ่านทางเว็บไซต์นี้ แม้ความเสียหายจะส่งผลให้สูญเสียกำไร, เกิดความขัดข้องทางธุรกิจ, สูญเสียบางส่วนหรือทั้งหมดของโปรแกรมหรือข้อมูลที่เก็บรักษาในคอมพิวเตอร์หรือที่อื่น โดยไม่จำกัดวงความเสียหาย</p>



<p>การดาวน์โหลดและพิมพ์เนื้อหาจากเว็บไซต์</p>

<p>ตัวหนังสือและกราฟิคทั้งหมดที่ปรากฎบนเว็บไซต์นี้ และ ที่ถูกคัดเลือกหรือจัดเรียงมา เป็นลิขสิทธิ์ของ GO Sport คุณได้รับอนุญาตให้ทำซ้ำทางอิเล็กทรอนิคส์และพิมพ์ลงบนกระดาษเพื่อใช้ส่วนบุคคลและไม่เป็นเชิงพาณิชย์ การใช้เนื้อหาใดๆ บนเว็บไซต์นี้ (รวมถึงแต่ไม่จำกัดเพียงการทำซ้ำเพื่อจุดประสงค์อื่นๆ นอกจากระบุข้างต้น และดัดแปลง, เผยแพร่ หรือตีพิมพ์) โดยไม่ได้รับอนุญาตเป็นลายลักษณ์อักษรจาก GO Sport นั้นเป็นสิ่งต้องห้ามอย่างเด็ดขาด</p>



<p>ทรัพย์สินทางปัญญา</p>

<p>ทุกเครื่องหมายการค้า, ชื่อผลิตภัณฑ์, ชื่อแบรนด์, ชื่อบริษัท, ชั้นยศ, สิ่งสงวนลิขสิทธิ์ และตราสัญลักษณ์ที่ปรากฎในนี้ เป็นเครื่องหมายการค้า, ชื่อทางการค้า หรือลิขสิทธิ์แห่งผู้เป็นเจ้าของ </p>
<p>ไม่มีการอนุญาตจาก GO Sport ให้นำเครื่องหมายการค้า, ชื่อสินค้า, ชั้นยศ, สิ่งสงวนลิขสิทธิ์ หรือตราสัญลักษณ์ใดๆ ไปใช้งาน</p>



<p>ความเห็นผู้ใช้, การตอบกลับ และข้อมูลอื่น ๆ ที่ส่งมา</p>

<p>ความเห็น, การตอบกลับ, ข้อเสนอแนะ, ความคิด และข้อมูลอื่น ๆ ที่จัดส่งมาโดยเปิดเผย, จัดส่งหรือมอบให้ GO Sport บนเว็บไซต์หรือโดยเว็บไซต์ของเรา หรือเปิดเผยในทางอื่น, จัดส่งหรือมอบให้ผ่านการใช้งานเว็บไซต์ของเรา (จะอ้างถึงว่า “ความเห็น”) จะตกเป็นทรัพย์สินของ GO Sport การเปิดเผย, จัดส่ง หรือส่งมอบความเห็นใด ๆ จะถือว่ามอบเป็นสิทธิ์ให้ GO Sport ในการถือสิทธิ์ทั่วโลก, ชื่อ, ผลประโยชน์จากลิขสิทธิ์ทั้งหมด และทรัพย์สินทางปัญญาทั้งหมดในความเห็น</p>

<p>GO Sport จะเป็นเจ้าของแต่เพียงผู้เดียวในสิทธิ์, ชื่อ, และผลประโยชน์ และใช้งานได้โดยไม่จำกัด ทั้งเชิงพาณิชย์และอื่น ๆ ของความเห็นใดๆ GO Sport ไม่และจะไม่มีหน้าที่ในการ (a) <p>รักษาความเห็นใด ๆ เป็นความลับ; (b) จ่ายค่าชดเชยใด ๆ สำหรับความเห็น; หรือ (c) ตอบกลับความเห็นใด ๆ ของผู้ใช้</p>

<p>คุณเห็นชอบว่า ความเห็นที่ส่งมาโดยคุณถึงเว็บไซต์ของเรา จะไม่ละเมิดสิทธิ์ของบุคคลที่สามใดๆ รวมไปถึงลิขสิทธิ์, เครื่องหมายการค้า, ความเป็นส่วนตัว หรือสิทธิ์ความเป็นเจ้าของ </p>นอกจากนี้ คุณเห็นชอบว่าความเห็นที่คุณส่งมาจะไม่เป็นหรือมีการทำให้เสียชื่อเสียง หรือมีเนื้อหาผิดกฎหมาย, หยามเหยียด หรือไม่เหมาะสม </p>
<p>คุณจะเป็นผู้รับผิดชอบแต่เพียงผู้เดียวต่อเนื้อหาหรือความเห็นใด ๆ ที่คุณแสดง
คุณยินยอมให้ GO Sport อาจใช้ และ/หรือ เปิดเผยข้อมูลเชิงประชากรของคุณ และข้อมูลการใช้งานเว็บไซต์เราของคุณในรูปแบบใดๆ ที่ไม่เปิดเผยตัวตนของคุณ การเข้าร่วมกิจกรรมในเว็บไซต์ของเรา, กิจกรรมโปรโมชัน และ/หรือ ร้องขอข้อมูลโปรโมชัน หรืออัพเดตผลิตภัณฑ์ ถือเป็นยอมรับว่า GO Sport อาจใช้ข้อมูลของคุณสำหรับการทำการตลาด, โปรโมชัน และจุดประสงค์ทางธุรกิจอื่นๆ</p>
 

