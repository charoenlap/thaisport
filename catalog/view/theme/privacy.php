<p>บริษัท โกสปอร์ต  จำกัด</p>

<p>ขอแจ้งนโยบายการคืนเงินเพื่อเป็นข้อตกลงและสร้างความเข้าใจเกี่ยวกับการใช้บริการเว็บไซต์ ดังนี้</p>

<p>1. บริษัทฯ จะทำการคืนเงินค่าสินค้าให้กับลูกค้า ในกรณีที่บริษัทฯ ไม่สามารถจัดการแข่งขัน หรือจัดส่งสินค้าตามที่ลูกค้าสั่งซื้อได้</p>

<p>1.1  ระยะเวลาการคืนเงินค่าสินค้า มีรายละเอียดดังนี้</p>

<p>กรณีลูกค้าชำระเงินเต็มจำนวนตัดผ่านระบบ บัตรเครดิต/เดบิต ( Visa , Master card ) จะทำการคืนเงินกลับไปยังบัตรเครดิตที่ลูกค้าใช้ในการชำระเงิน โดยใช้ระยะเวลาประมาณ 10-45 วัน ทำการนับตั้งแต่วันที่บริษัทฯ ได้รับเอกสารครบ*</p>

<p>ตามแต่เงื่อนไขของธนาคารเจ้าของบัตรเครดิต ซึ่งจะทำการคืนเงินกลับไปยังบัตรเครดิตของท่าน ท่านจะได้รับ Email</p>

<p>แจ้งจากทางบริษัทได้ดำเนินการโอนเงินคืนไปยังบัญชีท่าน</p>

<p>1.2  กรณีลูกค้าชำระเงินเต็มจำนวน ผ่านการโอนเงินเข้าบัญชีธนาคาร ธนาคารกสิกร ชื่อบัญชี นางสาวลลนา ประเสริฐโชค ของบริษัทโกสปอร์ตจำกัด บริษัทจะทำการคืนเงินเต็มจำนวน ผ่านการโอนเงินคืนตามหมายเลขบัญชีธนาคารตามที่ท่านต้องการ</p>

<p>โดยใช้ระยะเวลาประมาณ 3-7 วันทำการ ท่านจะได้รับ Email ตอบกลับหลังจากการแจ้งการคืนเงินภายในระยะเวลา 24 ชม.</p>



<p>2. การแจ้งขอเงินคืนลูกค้า ในกรณีที่ลูกค้าต้องการยกเลิกคำสั่งซื้อ ในระยะเวลาไม่เกิน 24 ชั่วโมง</p>

<p>2.1  ระยะเวลาการคืนเงินค่าสินค้า มีรายละเอียดดังนี้</p>

<p>กรณีลูกค้าชำระเงินเต็มจำนวนค่าสินค้า ตัดผ่านระบบ บัตรเครดิต/เดบิต ( Visa , Master Card )</p>

<p>จะทำการคืนเงินกลับไปยังบัตรเครดิตที่ลูกค้าใช้ในการชำระเงิน โดยใช้ระยะเวลาประมาณ 10-45 วัน ทำการ</p>

<p>ตามแต่เงื่อนของธนาคารเจ้าของบัตรเครดิต</p>

<p>โดยมีการหักค่าธรรมเนียม Bank Charge เป็นจำนวน 4.5% จากยอดเงินที่ชำระเข้ามา</p>

<p>ซึ่งจะทำการคืนเงินกลับไปยังบัตรเครดิตของท่าน ท่านจะได้รับ Email แจ้งหลังจากที่บริษัทได้ดำเนินการโอนเงินคืนไปยังบัญชีของท่าน</p>

<p>2.2   กรณีลูกค้าชำระเงินเต็มจำนวน ผ่านการโอนเงินเข้าบัญชีธนาคารของ บริษัท โกสปอร์ต จำกัด</p>

<p>บริษัทจะทำการคืนเงินโดยหักค่าธรรมเนียมตามรายการ ผ่านการโอนเงินคืนตามหมายเลขบัญชีธนาคารตามที่ท่านต้องการ</p>

<p>โดยใช้ระยะเวลาประมาณ 3 – 7 วันทำการ</p>

<p>3. บริษัทฯ ขอสงวนสิทธิในการเปลี่ยนแปลงเงื่อนไขใดๆ โดยไม่จำต้องแจ้งให้ทราบล่วงหน้า </p>

<p>4. บริษัทฯ ขอสงวนสิทธิในการคืนเงินและยกเลิกคำสั่งซื้อหากระยะเวลาเกิน 24 ชม. นับจากการสั่งซื้อ</p>

<p>5. บริษัทฯ ขอสงวนสิทธิ์ในการคืนเงินเกินระยะเวลาที่กำหนด หากเกิดเหตุสุดวิสัย</p>



